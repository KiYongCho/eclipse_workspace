package oop;

// 동물 인터페이스
public interface IAnimal {
	
	// 숨쉬기 기능
	public abstract void breath();
	
	// 먹기 기능
	public abstract void eat();
	
	// 움직이기 기능
	public abstract void move();
	
	// 배설하기 기능
	public abstract void poo();
	
	// 소리내기 기능
	public abstract void sound();
	
	// 잠자기 기능
	public abstract void sleep();

}














